package Entidades;

public class Clientes {
    private String _IDCliente;
    private String _Nombres;
    private String _Apellidos;
    private String _DUI;
    private String _NIT;
    private String _Telefono;

    public String get_IDCliente() {
        return _IDCliente;
    }

    public void set_IDCliente(String _IDCliente) {
        this._IDCliente = _IDCliente;
    }

    public String get_Nombres() {
        return _Nombres;
    }

    public void set_Nombres(String _Nombres) {
        this._Nombres = _Nombres;
    }

    public String get_Apellidos() {
        return _Apellidos;
    }

    public void set_Apellidos(String _Apellidos) {
        this._Apellidos = _Apellidos;
    }

    public String get_DUI() {
        return _DUI;
    }

    public void set_DUI(String _DUI) {
        this._DUI = _DUI;
    }

    public String get_NIT() {
        return _NIT;
    }

    public void set_NIT(String _NIT) {
        this._NIT = _NIT;
    }

    public String get_Telefono() {
        return _Telefono;
    }

    public void set_Telefono(String _Telefono) {
        this._Telefono = _Telefono;
    }
}
