package Entidades;

public class Proveedores {
    private String _IDProveedor;
    private String _Proveedor;
    private String _NombreContacto;
    private String _Telefono;
    private String _Correo;
    private String _Direccion;

    public String get_IDProveedor() {
        return _IDProveedor;
    }

    public void set_IDProveedor(String _IDProveedor) {
        this._IDProveedor = _IDProveedor;
    }

    public String get_Proveedor() {
        return _Proveedor;
    }

    public void set_Proveedor(String _Proveedor) {
        this._Proveedor = _Proveedor;
    }

    public String get_NombreContacto() {
        return _NombreContacto;
    }

    public void set_NombreContacto(String _NombreContacto) {
        this._NombreContacto = _NombreContacto;
    }

    public String get_Telefono() {
        return _Telefono;
    }

    public void set_Telefono(String _Telefono) {
        this._Telefono = _Telefono;
    }

    public String get_Correo() {
        return _Correo;
    }

    public void set_Correo(String _Correo) {
        this._Correo = _Correo;
    }

    public String get_Direccion() {
        return _Direccion;
    }

    public void set_Direccion(String _Direccion) {
        this._Direccion = _Direccion;
    }
}
